"use client";

import { useState } from "react";
import { PlusCircle, Trash2, Pencil, X } from "lucide-react";
import Sidebar from "@/components/Sidebar";
import Header from "@/components/Header";
import { addUser, updateUser } from "../../service/accountService";

export default function AccountUI({
  user,
  handleLogout,
  data,
  loaiTaiKhoans,
  permissions,
  onDelete,
  onSubmitSuccess,
}) {
  const [formData, setFormData] = useState({
    id: "",
    tenTaiKhoan: "",
    hoTen: "",
    matKhau: "",
    LoaiTK_Id: "",
  });
  const [isEdit, setIsEdit] = useState(false);
  const [showForm, setShowForm] = useState(false);
  const [searchTerm, setSearchTerm] = useState("");  

  const handleInput = (e) => {
    const { name, value } = e.target;
    setFormData((prev) => ({
      ...prev,
      [name]: name === "LoaiTK_Id" ? Number(value) : value,
    }));
  };
  const handleSubmit = async () => {
    if (isEdit) {
      await updateUser(formData.id, formData);
    } else {
      await addUser(formData);
    }
    setFormData({ id: null, tenTaiKhoan: "", hoTen: "", matKhau: "", LoaiTK_Id: "" });
    setIsEdit(false);
    setShowForm(false);
    onSubmitSuccess();
  };
  const filteredData = (data || []).filter(
    (tk) =>
      tk.tenTaiKhoan.toLowerCase().includes(searchTerm.toLowerCase())
  );
  //  L·ªçc lo·∫°i t√†i kho·∫£n ƒë·ªÉ d√πng trong select
  const filteredLoaiTaiKhoans = loaiTaiKhoans.filter((l) => l.ten !== "Admin");
  return (
    <div className="flex min-h-screen bg-gray-100 text-gray-900 font-sans">
      <Sidebar user={user} />
      <div className="flex-1 px-8 py-6">
        <Header user={user} onLogout={handleLogout} />
        <h2 className="text-4xl font-bold text-orange-600 mb-8">Qu·∫£n l√Ω t√†i kho·∫£n</h2>

        <div className="p-6 space-y-6 bg-white rounded-xl shadow-lg border border-purple-200">
          {/* Search + Add */}
          <div className="flex justify-between items-center mb-4">
            <input
              type="text"
              placeholder="üîç T√¨m ki·∫øm t√†i kho·∫£n..."
              className="input input-bordered w-full max-w-md"
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
            />
            {permissions.Them && (
              <button
                className="ml-4 px-4 py-2 bg-orange-500 text-white rounded-full flex items-center gap-2"
                onClick={() => {
                  setIsEdit(false);
                  setShowForm(true);
                  setFormData({ id: null, tenTaiKhoan: "", hoTen: "", matKhau: "", LoaiTK_Id: "" });
                }}
              >
                <PlusCircle size={18} /> Th√™m t√†i kho·∫£n
              </button>
            )}
          </div>

          {/* Data Table */}
          {permissions.Xem ? (
            <div className="overflow-auto">
              <table className="w-full bg-white shadow rounded">
                <thead className="bg-gradient-to-r from-fuchsia-500 to-pink-500 text-white">
                  <tr>
                    <th className="p-3 text-left">T√™n ƒëƒÉng nh·∫≠p</th>
                    <th className="p-3 text-left">H·ªç t√™n</th>
                    <th className="p-3 text-left">Lo·∫°i t√†i kho·∫£n</th>
                    {(permissions.Sua || permissions.Xoa) && (
                      <th className="p-3 text-left">Thao t√°c</th>
                    )}
                  </tr>
                </thead>
                <tbody>
                  {filteredData.map((tk) => {
                    const loai = loaiTaiKhoans.find((l) => l.id === tk.LoaiTk_Id);                    
                    return (
                      <tr key={tk.id} className="hover:bg-green-100">
                        <td className="p-3">{tk.tenTaiKhoan}</td>
                        <td className="p-3">{tk.hoTen}</td>
                        <td className="p-3">{loai?.ten || "kh√¥ng t√¨m th·∫•y"}</td>
                        {(permissions.Sua || permissions.Xoa) && (
                          <td className="space-x-2 p-3">
                            {permissions.Sua && (
                              <button
                                className="text-blue-600"
                                onClick={() => {
                                  setIsEdit(true);
                                  setShowForm(true);
                                  setFormData({ ...tk });
                                }}
                              >
                                <Pencil size={18} /> S·ª≠a
                              </button>
                            )}
                            {permissions.Xoa && (
                              <button
                                className="text-red-600"
                                onClick={() => onDelete(tk.id)}
                              >
                                <Trash2 size={18} /> X√≥a
                              </button>
                            )}
                          </td>
                        )}
                      </tr>
                    );
                  })}
                </tbody>
              </table>
            </div>
          ) : (
            <div className="text-center text-red-600 font-bold mt-4">
              <X size={18} /> B·∫°n kh√¥ng c√≥ quy·ªÅn xem d·ªØ li·ªáu!
            </div>
          )}

          {/* Add/Edit Form */}
          {(permissions.Them || permissions.Sua) && showForm && (
            <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/50">
              <div className="bg-white rounded-2xl p-8 w-full max-w-xl">
                <h2 className="text-2xl font-semibold text-orange-600 mb-6">
                  {isEdit ? "S·ª≠a" : "Th√™m"} t√†i kho·∫£n
                </h2>
                <div className="space-y-4">
                  <input
                    name="tenTaiKhoan"
                    placeholder="T√™n ƒëƒÉng nh·∫≠p"
                    className="w-full px-4 py-2 border rounded-lg"
                    value={formData.tenTaiKhoan}
                    onChange={handleInput}
                  />
                  <input
                    name="hoTen"
                    placeholder="H·ªç t√™n"
                    className="w-full px-4 py-2 border rounded-lg"
                    value={formData.hoTen}
                    onChange={handleInput}
                  />
                  <input
                    name="matKhau"
                    type="password"
                    placeholder="M·∫≠t kh·∫©u"
                    className="w-full px-4 py-2 border rounded-lg"
                    value={formData.matKhau}
                    onChange={handleInput}
                  />
                  <select
                    name="LoaiTK_Id"
                    className="w-full px-4 py-2 border rounded-lg"
                    value={formData.LoaiTK_Id}
                    onChange={handleInput}
                  >
                    <option value="">-- Ch·ªçn lo·∫°i t√†i kho·∫£n --</option>
                    {filteredLoaiTaiKhoans.map((l) => (
                      <option key={l.id} value={l.id}>
                        {l.ten}
                      </option>
                    ))}
                  </select>
                </div>
                <div className="flex justify-end gap-4 mt-6">
                  <button
                    className="px-6 py-2 bg-orange-500 text-white rounded"
                    onClick={handleSubmit}
                  >
                    {isEdit ? "C·∫≠p nh·∫≠t" : "Th√™m"}
                  </button>
                  <button
                    className="px-6 py-2 border rounded"
                    onClick={() => setShowForm(false)}
                  >
                    H·ªßy
                  </button>
                </div>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}
